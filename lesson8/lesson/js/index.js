document.addEventListener("DOMContentLoaded",function(){
	
	
	
	var xDiv = document.getElementById('mainDiv');
	
	xDiv.addEventListener( "mouseover" , function () {
		
		xDiv.style.backgroundColor = "blue";
		
	} );
	
	
	xDiv.addEventListener( "mouseout" , function () {
		
		xDiv.style.backgroundColor = "green";
		
	} );
	
	
	
	
	
});

	